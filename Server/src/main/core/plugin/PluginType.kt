package core.plugin;

/**
 * Represents the plugin types.
 * @author Emperor
 */
enum class PluginType {
    /**
     * Represents an action plugin type.
     */
    ACTION,

    /**
     * Represents a dialogue plugin type.
     */
    DIALOGUE,

    /**
     * Represents an activity plugin type.
     */
    ACTIVITY,

    /**
     * Represents a login plugin type.
     */
    LOGIN,

    /**
     * Represents a logout plugin type.
     */
    LOGOUT,

    /**
     * Represents a quest plugin type.
     */
    QUEST
}
