package core.api

/**
 * Input type
 *
 * @constructor Input type
 */
enum class InputType {
    /**
     * Amount
     *
     * @constructor Amount
     */
    AMOUNT,

    /**
     * Numeric
     *
     * @constructor Numeric
     */
    NUMERIC,

    /**
     * String Short
     *
     * @constructor String Short
     */
    STRING_SHORT,

    /**
     * String Long
     *
     * @constructor String Long
     */
    STRING_LONG,

    /**
     * Message
     *
     * @constructor Message
     */
    MESSAGE
}