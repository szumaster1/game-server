package core.game.container

/**
 * Container type
 *
 * @constructor Container type
 */
enum class ContainerType {
    /**
     * Default
     *
     * @constructor Default
     */
    DEFAULT,

    /**
     * Shop
     *
     * @constructor Shop
     */
    SHOP,

    /**
     * Always Stack
     *
     * @constructor Always Stack
     */
    ALWAYS_STACK,

    /**
     * Never Stack
     *
     * @constructor Never Stack
     */
    NEVER_STACK
}