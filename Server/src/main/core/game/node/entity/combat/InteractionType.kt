package core.game.node.entity.combat

/**
 * Interaction type.
 */
enum class InteractionType {
    /**
     * Still Interact
     *
     * @constructor Still Interact
     */
    STILL_INTERACT,

    /**
     * Move Interact
     *
     * @constructor Move Interact
     */
    MOVE_INTERACT,

    /**
     * No Interact
     *
     * @constructor No Interact
     */
    NO_INTERACT
}