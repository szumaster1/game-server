package core.game.world.update.flag

/**
 * Entity flag.
 */
enum class EntityFlag {
    /**
     * Chat.
     */
    Chat,

    /**
     * Force chat.
     */
    ForceChat,

    /**
     * Primary hit.
     */
    PrimaryHit,

    /**
     * Secondary hit.
     */
    SecondaryHit,

    /**
     * Animate.
     */
    Animate,

    /**
     * Spot anim.
     */
    SpotAnim,

    /**
     * Appearance.
     */
    Appearance,

    /**
     * Face entity.
     */
    FaceEntity,

    /**
     * Face location.
     */
    FaceLocation,

    /**
     * Force move.
     */
    ForceMove,

    /**
     * Anim seq.
     */
    AnimSeq,

    /**
     * Type swap.
     */
    TypeSwap
}
